test_list = ['https', 'www', 'python', 'izm', 'com']
 
print(test_list[:4])

#開始位置を省略し、終了位置を指定することで先頭から指定位置までの要素を取得することができます。





test_list = ['https', 'www', 'python', 'izm', 'com']
 
print(test_list[2:])

#開始位置を指定し、終了位置を省略することで指定位置から末尾までの要素を取得することができます。





test_list = ['https', 'www', 'python', 'izm', 'com']
 
print(test_list[::2])

#ステップ幅を指定することで、指定数ごとの要素を取得することができます。

